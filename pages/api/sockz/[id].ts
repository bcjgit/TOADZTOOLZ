let wallet: any;

async function checkSockzOwner(id: string) {
  const sockzConnected = sockz.connect(wallet);

  try {
    let result = await sockzConnected.ownerOf(id);

    if (result) {
      return true;
    }
  } catch {
    return false;
  }
}
async function checkChoadzOwner(id: string) {
  const choadzConnected = choadz.connect(wallet);

  try {
    let result = await choadzConnected.ownerOf(id);

    if (result) {
      return true;
    }
  } catch {
    return false;
  }
}

async function checkFlyzOwner(id: string) {
  const flyzConnected = flyz.connect(wallet);

  try {
    let result = await flyzConnected.ownerOf(id);

    if (result) {
      return true;
    }
  } catch {
    return false;
  }
}

export default async (req: any, res: any) => {
  const {
    query: { id },
    method,
  } = req;

  const sockzId = parseInt(id);

  switch (method) {
    case "GET":
      wallet = ethers.Wallet.createRandom().connect(provider);

      let sockzMinted = await checkSockzOwner(ethers.utils.hexlify(sockzId));
      let choadzMinted = await checkChoadzOwner(ethers.utils.hexlify(sockzId));
      let flyzMinted = await checkFlyzOwner(ethers.utils.hexlify(sockzId));

      res.status(200).json({
        sockzMinted,
        choadzMinted,
        flyzMinted,
      });
  }
};
